    private void btnRegProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegProActionPerformed
        String tN = tfNomP.getText();
        String tPa = tfPa.getText();
        String tTel = tfTel.getText();
        String tCE = tfCoEl.getText();
        
        if(tN.length()==0 || tPa.length()==0 || tTel.length()==0 || tCE.length()==0){
             JOptionPane.showMessageDialog(null,"Faltan datos","ALERTA",JOptionPane.INFORMATION_MESSAGE);   
        }
        
        try {
            Connection con;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:4306/groovemusic","root","");
            Statement st;
            ResultSet rs;
            st=con.createStatement();
            st.executeUpdate("INSERT INTO proveedores (ID, Nombre, Pais, Telefono, CorreoElectronico) VALUES (NULL,'"+tN+"','"+tPa+"','"+tTel+"','"+tCE+"');");
            con.close();
            JOptionPane.showMessageDialog(null,"Proveedor agregado","ALERTA",JOptionPane.INFORMATION_MESSAGE); 
        } catch (Exception e) {
            System.out.println("Error: "+e);
            JOptionPane.showMessageDialog(null,"Error al agregrar proveedor","ALERTA",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnRegProActionPerformed

