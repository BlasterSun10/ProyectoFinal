    public InterazConsultarArticulos() {
        initComponents();
        try {
            Connection con;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:4306/groovemusic","root","");
            Statement st;
            ResultSet rs;
            st=con.createStatement();
            rs=st.executeQuery("SELECT articulos.Nombre, articulos.Descri, articulos.Precio, articulos.Marca, articulos.NoExistencias, proveedores.Nombre FROM articulos INNER JOIN artxprov ON articulos.IDArticulo=artxprov.IDArt  INNER JOIN proveedores ON proveedores.ID=artxprov.IDProv");
            
            DefaultTableModel model = new DefaultTableModel();

            model.addColumn("Articulo");
            model.addColumn("Descripcion");
            model.addColumn("Precio");
            model.addColumn("Marca");
            model.addColumn("No. Existencias");
            model.addColumn("Proovedor");
            tbArticulos.setModel(model);
            
            String [] datos = new String[6];
            
            while (rs.next()) {                
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                model.addRow(datos);
                
            }    
            con.close();
        } catch (Exception e) {
            System.out.println("Error: "+e);
            JOptionPane.showMessageDialog(null,"Error al mostrar tabla","ALERTA",JOptionPane.INFORMATION_MESSAGE); 
        } 
    }

