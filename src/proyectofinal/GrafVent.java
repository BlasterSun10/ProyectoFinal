    public GrafVent() {
        initComponents();

        DefaultPieDataset datos = new DefaultPieDataset();
        
        
        try {
            Connection con;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:4306/groovemusic","root","");
            Statement st;
            ResultSet rs;
            st=con.createStatement();
            rs=st.executeQuery("SELECT articulos.Nombre, COUNT(Cantidad) AS 'Cantidad' FROM clixart INNER JOIN articulos ON clixart.IDArticulo=articulos.IDArticulo GROUP BY (clixart.IDArticulo);");
            
            while (rs.next()) {       
                String Val=rs.getString("Cantidad");
                int c = Integer.parseInt(Val);
                datos.setValue(rs.getString("Nombre")+" "+rs.getString("Cantidad"), c);
            }    
            con.close();
        } catch (Exception e) {
            System.out.println("Error: "+e);
            JOptionPane.showMessageDialog(null,"Error al mostrar tabla","ALERTA",JOptionPane.INFORMATION_MESSAGE); 
        }

        JFreeChart grafico_circular = ChartFactory.createPieChart("Grafico Ventas", datos, true, true, false);
        
        ChartPanel panel = new ChartPanel(grafico_circular);
        panel.setMouseWheelEnabled(true);
        panel.setPreferredSize(new Dimension(1000,500));
        
        PanGraf.setLayout(new BorderLayout());
        PanGraf.add(panel,BorderLayout.NORTH);
        pack();
        repaint();
    }

